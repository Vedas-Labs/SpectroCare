package com.vedas.spectrocare.model;

public class MedicinesListviewModel {
    String purpose;
    String medicineName;
    String medicineDosage;
    String medicineDuration;
    String medicineFrequency;
    String medicineInformation;

    public String getPurpose() {
        return purpose;
    }

    public void setPurpose(String purpose) {
        this.purpose = purpose;
    }

    public String getMedicineName() {
        return medicineName;
    }

    public void setMedicineName(String medicineName) {
        this.medicineName = medicineName;
    }

    public String getMedicineDosage() {
        return medicineDosage;
    }

    public void setMedicineDosage(String medicineDosage) {
        this.medicineDosage = medicineDosage;
    }

    public String getMedicineDuration() {
        return medicineDuration;
    }

    public void setMedicineDuration(String medicineDuration) {
        this.medicineDuration = medicineDuration;
    }

    public String getMedicineFrequency() {
        return medicineFrequency;
    }

    public void setMedicineFrequency(String medicineFrequency) {
        this.medicineFrequency = medicineFrequency;
    }

    public String getMedicineInformation() {
        return medicineInformation;
    }

    public void setMedicineInformation(String medicineInformation) {
        this.medicineInformation = medicineInformation;
    }
}
