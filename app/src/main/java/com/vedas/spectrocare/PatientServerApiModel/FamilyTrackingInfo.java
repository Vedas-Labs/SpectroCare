package com.vedas.spectrocare.PatientServerApiModel;

public class FamilyTrackingInfo {
    String info;
    String byWhom;
    String byWhomName;
    String byWhomID;
    String date;

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public String getByWhom() {
        return byWhom;
    }

    public void setByWhom(String byWhom) {
        this.byWhom = byWhom;
    }

    public String getByWhomName() {
        return byWhomName;
    }

    public void setByWhomName(String byWhomName) {
        this.byWhomName = byWhomName;
    }

    public String getByWhomID() {
        return byWhomID;
    }

    public void setByWhomID(String byWhomID) {
        this.byWhomID = byWhomID;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
